        <li><strong>Email provider:</strong> a transactional mail provider used to send order and download communications.</li>
        <li><strong>Professional advisers:</strong> lawyers, accountants, auditors, insurers, or other advisers where reasonably necessary.</li>
        <li><strong>Authorities and affected parties:</strong> where required by law or reasonably necessary to protect rights, safety, security, users, or the public.</li>
        <li><strong>Business transaction recipients:</strong> participants in a merger, financing, reorganization, sale, or transfer involving all or part of the project, subject to appropriate safeguards.</li>
