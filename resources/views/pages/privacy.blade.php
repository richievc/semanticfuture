    <h2>1. Information we collect</h2>

    <h3>Information you provide</h3>
    <ul>
        <li><strong>Account information:</strong> your name, email address, encrypted password, and account timestamps.</li>
        <li><strong>Contact information:</strong> your name, email address, message, and any information you choose to include in a support or contact request.</li>
        <li><strong>Order information:</strong> order number, purchased product, price, currency, order status, and dates associated with payment, refund, and access.</li>
        <li><strong>Communications:</strong> records needed to answer account, delivery, refund, legal, or support requests.</li>
    </ul>

    <h3>Payment information</h3>
    <p>
        PayPal processes payments on its systems. We do not directly collect or store your complete payment-card
        number or bank-account credentials. We receive transaction identifiers, payment status, amount, currency,
        and other information needed to match and verify an order. Payment notifications retained for security and
        troubleshooting are designed to redact payer email, name, street address, telephone number, and similar
        payer details before storage in our application database.
    </p>

    <h3>Information collected automatically</h3>
    <ul>
        <li><strong>Session and security data:</strong> session identifiers, CSRF/security tokens, authentication state, and related cookie information.</li>
        <li><strong>Usage and transaction events:</strong> product views, checkout starts, completed or failed purchases, generated download links, and completed or repeated downloads.</li>
        <li><strong>Technical data:</strong> IP address, timestamps, requested pages, and standard server or application log information.</li>
        <li><strong>Download records:</strong> download counts, limits, grant or revocation status, and the date of the latest download.</li>
    </ul>

    <h2>2. How we use information</h2>
    <p>We use personal information to:</p>
    <ul>
        <li>create, authenticate, maintain, and secure customer accounts;</li>
        <li>create orders, confirm PayPal payments, deliver purchases, and enforce download limits;</li>
        <li>send transactional messages such as order confirmations and secure download links;</li>
        <li>answer contact, support, refund, account, and legal requests;</li>
        <li>detect fraud, investigate failures, prevent abuse, and protect the Services;</li>
        <li>measure storefront activity, purchases, revenue, and downloads through first-party analytics;</li>
        <li>maintain, troubleshoot, and improve the website and publications; and</li>
        <li>comply with legal, accounting, tax, and dispute-resolution obligations.</li>
    </ul>
    <p>
        We do not currently use third-party advertising trackers on the site, and we do not sell personal
        information or share it for cross-context behavioral advertising.
    </p>

    <h2>3. Cookies and similar technologies</h2>
    <p>
        We use first-party cookies and comparable browser mechanisms that are necessary to operate the site. These
        support login sessions, remember authenticated users where requested, protect forms against cross-site
        request forgery, retain short-lived checkout state, and provide security. Refusing these cookies through
        your browser may prevent account, checkout, contact, and download features from working correctly.
    </p>
    <p>
        Our current analytics are generated from first-party application events rather than third-party advertising
        cookies. If we add materially different analytics, advertising, or tracking technologies, we will update
        this Policy and provide any consent controls required by applicable law.
    </p>

