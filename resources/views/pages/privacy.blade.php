    <h2>5. Data retention</h2>
    <p>
        We retain information for as long as reasonably needed for the purposes described above. Account and access
        information is generally retained while an account remains active. Order, transaction, refund, and payment
        verification records may be retained longer to meet accounting, tax, fraud-prevention, chargeback, and legal
        requirements. Contact messages and technical logs are retained for support, security, and operational needs
        and then deleted or anonymized when they are no longer reasonably necessary.
    </p>
    <p>
        Backup copies may remain for a limited additional period before being overwritten. We may retain information
        needed to establish, exercise, or defend legal claims even after an account-deletion request.
    </p>

    <h2>6. Data security</h2>
    <p>
        We use administrative, technical, and organizational safeguards appropriate to the information we process.
        These include HTTPS in production, hashed passwords, encrypted production sessions, access controls,
        server-side payment verification, redaction of selected PayPal payer fields, private file storage, signed
        download links, download limits, and logging for fraud and security review. No method of transmission or
        storage is completely secure, so we cannot guarantee absolute security.
    </p>

